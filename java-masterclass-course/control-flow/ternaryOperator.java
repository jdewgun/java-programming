public class ternaryOperator {

    public static void main(String[] args) {
        int ageOfClient = 20;

        boolean isEighteenOrOver = ageOfClient == 20 ? true : false;

        if (isEighteenOrOver) {
            System.out.println("Client is over 18 Years Old");
        }
    }
}